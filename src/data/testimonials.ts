export function getTestimonialsByCategory(category: string): Testimonial[] {
  return testimonials.filter((t) => t.category === category);
}

export function getFeaturedTestimonials(): Testimonial[] {
  return testimonials.filter((t) => t.isFeatured);
}

export function getTestimonialBySlug(slug: string): Testimonial | undefined {
  return testimonials.find((t) => t.slug === slug);
}

export function getCategoryCounts(): Record<string, number> {
  const counts: Record<string, number> = {};
  testimonials.forEach((t) => {
    counts[t.category] = (counts[t.category] || 0) + 1;
  });
  return counts;
}

